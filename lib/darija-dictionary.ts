/**
 * Normalizes input text by mapping recognized Darija terms to their canonical equivalents.
 * Splits text into words, normalizes each word using the dictionary, and joins them back.
 * Handles punctuation and downcasing.
 */
export function normalizeDarija(text: string): string {
  if (!text) return "";
  
  // Clean text: downcase, strip punctuation but keep numbers and standard letters
  const cleanedText = text
    .toLowerCase()
    .replace(/[.,\/#!$%\^&\*;:{}=\-_`~()?]/g, " ")
    .replace(/\s+/g, " ")
    .trim();

  const words = cleanedText.split(" ");
  const normalizedWords = words.map(word => {
    // Exact match in dictionary
    if (DARIJA_DICTIONARY[word]) {
      return DARIJA_DICTIONARY[word];
    }
    
    // Fuzzy matching for some common suffix variations (e.g. "dyali", "dyalna")
    if (word.startsWith("dyal")) {
      return "de";
    }

    return word;
  });

  return normalizedWords.join(" ");
}
